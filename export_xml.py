"""
Use this script to export the split XML stored in the SQLITE database file 
generated by dump_splitter.py, into a .xml flatfile that is ready to import 
into a MediaWiki instance. 

Config:
 - INPUT_SPLIT_DUMP: The filepath of the SQLITE database file (generated by dump_splitter.py)
 - OUTPUT_DIRECTORY: The file directory of the output XML file
 - FILENAME_PREFIX: The filename of the output XML file will be named after this config variable. 
                    Multiple XML files may be generated. In this case the files will be numbered "_1", "_2", etc.
 - MAX_NUMBER_OF_PAGES_PER_XML: Set the maximum limit of number of pages to export per XML file, 
                                e.g. you can set the script to export the wikipages into several XML files with a 
                                     maximum of 100 pages per file.

 - ONLY_IN_NAMESPACES: Only for these namespaces, e.g. 0 = Main article.
                       Leave empty if not filtering for namespace.
 - ONLY_PAGEIDS: Only for the following page IDs. 
                 Leave empty if not filtering for page ID.
 - EXCLUDE_PAGEIDS: Exclude these page IDs.
                    Leave empty if not filtering for page ID.
 - EXPORT_REDIRECTS:  Set to 0 if you want to export non-redirects
                      Set to 1 if you want to export redirects only
                      Set to 2 if you want to export all pages, including redirects

 - GET_NUMBER_OF_ITEMS_PER_DB_REQ: Adjust based on your computer's RAM
 - SITEINFO_XML: Adjust based on your wiki's siteinfo
"""

