def generate_sql_query(for_count: bool, limit: int, offset: int) -> str:
  query = f"SELECT {"COUNT(w.id)" if for_count else "w.id, w.contents"} FROM Wikipages w\n"
  where_clauses = []
  if len(ONLY_IN_NAMESPACES) > 0:
    where_clauses.append(f"w.ns IN ({','.join(map(lambda x: str(x), ONLY_IN_NAMESPACES))})")
  if len(ONLY_PAGEIDS) > 0:
    where_clauses.append(f"w.id IN ({','.join(map(lambda x: str(x), ONLY_PAGEIDS))})")
  if len(EXCLUDE_PAGEIDS) > 0:
    where_clauses.append(f"w.id NOT IN ({','.join(map(lambda x: str(x), EXCLUDE_PAGEIDS))})")
  if EXPORT_REDIRECTS == 0:
    where_clauses.append(f"w.redirect_to IS NULL")
  if EXPORT_REDIRECTS == 1:
    where_clauses.append(f"w.redirect_to IS NOT NULL")
  
  if len(where_clauses) > 0:
    query += "WHERE " + " AND ".join(where_clauses) + "\n"
  
  if not for_count:
    query += f"LIMIT {limit} OFFSET {offset}"

  query += ";"

  return query

def port_to_split_xml():
  db_conn = sqlite3.connect(INPUT_SPLIT_DUMP)
  db_cursor = db_conn.cursor()
  query = generate_sql_query(True, 0, 0)
  print(f"Executing query:\n{query}")
  num_items = db_cursor.execute(query).fetchone()[0]
  print(f"Found {num_items} items")

  num_text_files = num_items // MAX_NUMBER_OF_PAGES_PER_XML
  if (num_items % MAX_NUMBER_OF_PAGES_PER_XML > 0):
    num_text_files += 1

  for n_txtdump in range(num_text_files):
    filename = f"{FILENAME_PREFIX}-{n_txtdump+1}.xml"
    with open(f"{OUTPUT_DIRECTORY}{filename}", 'w', encoding='UTF-8') as f:
      f.write(f"""<mediawiki xmlns="http://www.mediawiki.org/xml/export-0.11/" xmlns:xsi="http://www.w3.org/2001/XMLSchema-instance" xsi:schemaLocation="http://www.mediawiki.org/xml/export-0.11/ http://www.mediawiki.org/xml/export-0.11.xsd" version="0.11" xml:lang="en">
  {SITEINFO_XML}\n  """)
      for n_dbreq in range(MAX_NUMBER_OF_PAGES_PER_XML // GET_NUMBER_OF_ITEMS_PER_DB_REQ):
        query = generate_sql_query(
          False, 
          GET_NUMBER_OF_ITEMS_PER_DB_REQ, 
          n_txtdump * MAX_NUMBER_OF_PAGES_PER_XML + n_dbreq * GET_NUMBER_OF_ITEMS_PER_DB_REQ
        )
        items = db_cursor.execute(query).fetchall()
        for _, contents in items:
          f.write(contents)
      f.write("</mediawiki>\n")
    print(f"Finished writing {filename}")

  db_conn.close()

if __name__ == "__main__":
  port_to_split_xml()
